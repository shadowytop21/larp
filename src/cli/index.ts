// ?????? Colours (simple, no chalk dep in CLI output) ???????????????????????????????????????????????????????????????????????????????????????
const c = {
  red:    (s: string) => `\x1b[31m${s}\x1b[0m`,
  green:  (s: string) => `\x1b[32m${s}\x1b[0m`,
  yellow: (s: string) => `\x1b[33m${s}\x1b[0m`,
  cyan:   (s: string) => `\x1b[36m${s}\x1b[0m`,
  bold:   (s: string) => `\x1b[1m${s}\x1b[0m`,
  gray:   (s: string) => `\x1b[90m${s}\x1b[0m`,
};

// ?????? Compiler pipeline ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
function compile(source: string, filePath: string): string {
  try {
    const tokens = tokenize(source);
    const ast    = parse(tokens);
    const js     = transpile(ast);
    return js;
  } catch (err: any) {
    const loc  = err.line ? ` (line ${err.line}, column ${err.col})` : '';
    const file = path.relative(process.cwd(), filePath);
    console.error(`\n${c.red(c.bold('Error'))} in ${c.cyan(file)}${loc}:`);
    console.error(`  ${err.message}`);
    if (err.fix) console.error(`\n${c.yellow('Fix')}: ${err.fix}`);
    if (process.argv.includes('--verbose')) console.error(err.stack);
    process.exit(1);
    throw err; // unreachable ??? satisfies TypeScript return type
  }
}

// ?????? run ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
async function cmdRun(filePath: string): Promise<void> {
  const absPath = path.resolve(filePath);
  if (!fs.existsSync(absPath)) {
    die(`File not found: ${filePath}\n  Check the file name and try again.`);
  }

  const source = fs.readFileSync(absPath, 'utf8');
  const js     = compile(source, absPath);

  const runtimeSrc = path.join(__dirname, '..', 'runtime', 'helpers.js');
  const stdlibSrc  = path.join(__dirname, '..', '..', 'stdlib');

  // Patch require paths to point to actual absolute paths
  const patchedJs = js
    .split(`'./runtime/helpers.js'`).join(JSON.stringify(runtimeSrc))
    .split(`'./stdlib/server.js'`).join(JSON.stringify(path.join(stdlibSrc, 'server.js')))
    .split(`'./stdlib/database.js'`).join(JSON.stringify(path.join(stdlibSrc, 'database.js')))
    .split(`'./stdlib/http.js'`).join(JSON.stringify(path.join(stdlibSrc, 'http.js')))
    .split(`'./stdlib/ai.js'`).join(JSON.stringify(path.join(stdlibSrc, 'ai.js')))
    .split(`'./stdlib/files.js'`).join(JSON.stringify(path.join(stdlibSrc, 'files.js')))
    .split(`'./stdlib/helpers.js'`).join(JSON.stringify(path.join(stdlibSrc, 'helpers.js')));

  // Execute directly in the same process
  const Module = require('module');
  const m = new Module(absPath, module);
  m.filename = absPath;
  m.paths = Module._nodeModulePaths(path.dirname(absPath));
  
  const originalArgv = process.argv.slice();
  // Find where the file name was passed in original args to correctly extract trailing args
  const fileArgIndex = process.argv.findIndex(a => a === filePath || path.resolve(a) === absPath);
  const trailingArgs = fileArgIndex !== -1 ? process.argv.slice(fileArgIndex + 1) : [];
  process.argv = [process.argv[0], absPath, ...trailingArgs];

  try {
    m._compile(patchedJs, absPath);
  } catch (e: any) {
    if (e.status !== undefined) {
      process.exit(e.status);
    }
    throw e;
  } finally {
    process.argv = originalArgv;
  }
}

// ?????? build ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
function cmdBuild(filePath: string): void {
  const absPath = path.resolve(filePath);
  if (!fs.existsSync(absPath)) die(`File not found: ${filePath}`);

  const source  = fs.readFileSync(absPath, 'utf8');
  const js      = compile(source, absPath);
  const outPath = absPath.replace(/\.larp$/, '.js');
  fs.writeFileSync(outPath, js, 'utf8');
  console.log(c.green(`??? Built: ${path.relative(process.cwd(), outPath)}`));
}

// ?????? test ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
async function cmdTest(dir: string): Promise<void> {
  const absDir = path.resolve(dir);
  const files  = findLarpFiles(absDir);

  if (files.length === 0) {
    console.log(c.yellow(`No .larp files found in ${dir}`));
    return;
  }

  let passed = 0, failed = 0;
  for (const f of files) {
    process.stdout.write(`  ${c.gray(path.relative(process.cwd(), f))}... `);
    try {
      await cmdRun(f);
      passed++;
      process.stdout.write(c.green('pass\n'));
    } catch {
      failed++;
      process.stdout.write(c.red('fail\n'));
    }
  }

  console.log(`\n${c.bold('Results')}: ${c.green(String(passed))} passed, ${failed > 0 ? c.red(String(failed)) : '0'} failed`);
  if (failed > 0) process.exit(1);
}

// ?????? format ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
function cmdFormat(filePath: string): void {
  const absPath = path.resolve(filePath);
  if (!fs.existsSync(absPath)) die(`File not found: ${filePath}`);

  const source = fs.readFileSync(absPath, 'utf8');
  // Basic formatter: normalize indentation (4 spaces), strip trailing spaces,
  // ensure single blank lines between blocks
  const lines    = source.split('\n');
  const out: string[] = [];
  let   depth  = 0;
  const INDENT = '    '; // 4 spaces

  for (const raw of lines) {
    const trimmed = raw.trim();
    if (!trimmed) { out.push(''); continue; }

    // Decrease indent before 'end', 'otherwise', 'otherwise if', 'if something goes wrong'
    if (/^(end|otherwise\b|if something goes wrong)/.test(trimmed)) {
      depth = Math.max(0, depth - 1);
    }

    out.push(INDENT.repeat(depth) + trimmed);

    // Increase indent after lines ending in ':'
    if (trimmed.endsWith(':')) depth++;
    // Decrease back after 'end' is placed
    if (/^end\b/.test(trimmed)) depth = Math.max(0, depth - 0); // already decreased
  }

  const formatted = out.join('\n').replace(/[ \t]+$/gm, '') + '\n';
  fs.writeFileSync(absPath, formatted, 'utf8');
  console.log(c.green(`✓ Formatted: ${filePath}`));
}

// ── install & init ────────────────────────────────────────────────────────────
function cmdInstall(): void {
  const manifestPath = path.resolve('larp.json');
  if (!fs.existsSync(manifestPath)) {
    console.log(c.yellow('No larp.json found in this directory. Run "larp init" to create one.'));
    return;
  }
  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  const deps = manifest.dependencies || {};
  const depNames = Object.keys(deps);
  if (depNames.length === 0) {
    console.log(c.green('✓ No dependencies to install.'));
    return;
  }
  console.log(c.cyan(`Installing ${depNames.length} dependencies...`));
  const { execSync } = require('child_process');
  try {
    execSync(`npm install ${depNames.map(d => `${d}@${deps[d]}`).join(' ')} --no-save`, { stdio: 'inherit' });
    console.log(c.green('✓ Dependencies installed successfully.'));
  } catch (err: any) {
    die('Failed to install dependencies.');
  }
}

function cmdInit(): void {
  const manifestPath = path.resolve('larp.json');
  if (fs.existsSync(manifestPath)) {
    console.log(c.yellow('larp.json already exists.'));
    return;
  }
  const manifest = {
    name: path.basename(process.cwd()),
    version: "1.0.0",
    description: "A LARP project",
    dependencies: {}
  };
  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2), 'utf8');
  console.log(c.green('✓ Created larp.json'));
}

// ?????? learn ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
async function cmdLearn(): Promise<void> {
  const { runLearn } = require('../repl/repl');
  await runLearn();
}

// ?????? Helpers ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
function findLarpFiles(dir: string): string[] {
  const results: string[] = [];
  if (!fs.existsSync(dir)) return results;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory())               results.push(...findLarpFiles(full));
    else if (entry.name.endsWith('.larp')) results.push(full);
  }
  return results;
}

function die(msg: string): never {
  console.error(c.red('Error') + ': ' + msg);
  process.exit(1);
  throw new Error(msg); // unreachable ??? satisfies TypeScript never return type
}

// ?????? Main ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
async function main(): Promise<void> {
  const args = process.argv.slice(2).filter(a => !a.startsWith('--'));
  const cmd  = args[0];

  switch (cmd) {
    case 'run':     await cmdRun(args[1] ?? die('Provide a file: larp run file.larp'));    break;
    case 'build':   cmdBuild(args[1] ?? die('Provide a file: larp build file.larp'));      break;
    case 'test':    await cmdTest(args[1] ?? '.');                                          break;
    case 'format':  cmdFormat(args[1] ?? die('Provide a file: larp format file.larp'));    break;
    case 'install': cmdInstall();                                                           break;
    case 'init':    cmdInit();                                                              break;
    case 'learn':   await cmdLearn();                                                       break;
    case 'version': console.log(`LARP v${VERSION}`);                                       break;
    default:
      console.log(HELP);
      if (cmd && cmd !== '--help' && cmd !== 'help') die(`Unknown command: ${cmd}`);
  }
}

main().catch((e: Error) => { console.error(e.message); process.exit(1); });
